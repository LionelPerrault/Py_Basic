floating_value = 10.5  # Declaring a floating value.

# Implementation of function as_integer_ratio():

""" The function returns a pair of integers whose ratio represents the corresponding integer or 3. float value, 
with a positive denominator. This is useful for converting floating-point numbers into a pair of integers for 
various purposes, such as for computation, storage, or transmission."""

print(floating_value.as_integer_ratio())

# Implementation of function hex():

""" Converts a floating point number into its equivalent hexadecimal representation.
"""

print(floating_value.hex())
